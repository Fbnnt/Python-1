animales = ["perro", "gato", "elefante", "tigre", "león"]

print(animales[2])

# Modificar el último elemento de la lista
animales[-1] = "jirafa"
print(animales)

# Añadir un nuevo elemento a la lista
animales.append("zorro")
print(animales)

# Insertar un nuevo elemento en la posición 2 de la lista
animales.insert(2, "oso")
print(animales)

# Eliminar un elemento de la lista por su valor
animales.remove("elefante")
print(animales)

# Eliminar un elemento de la lista por su índice
del animales[1]
print(animales)

# Ordenar la lista alfabéticamente
animales.sort()
print(animales)

# Ordenar la lista alfabéticamente en orden inverso
animales.reverse()
print(animales)
